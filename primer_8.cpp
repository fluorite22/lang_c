#include <stdio.h>
const int N = 10;
// размер массива
main()
{
int i, A[N];
// объявление массива
printf("Введите массив A\n"); // подсказка для ввода
for ( i = 0; i < N; i ++ ) { // цикл по всем элементам
printf("Введите A[%d]> ", i ); // подсказка для ввода A[i]
scanf ("%d", &A[i]); // ввод A[i]
}
for ( i = 0; i < N; i ++ ) // цикл по всем элементам
A[i] = A[i] * 2; // умножить A[i] на 2
printf("\nРезультат:\n");
for ( i = 0; i < N; i ++ ) // цикл по всем элементам
printf("%d ", A[i]); // вывести A[i]
}